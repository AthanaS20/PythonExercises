a = (1, 2, 3, 5)
b = (6, 8, 10)
c = a + b
#Você pode juntar duas tuplas criando outra variavel e somando as duas (a + b)

print(len(c)) #para mostrar quantos carcteres tem a variavel "c"
print(c.count(5)) #para mostrar quantas vezes aparece o número 5 na váriavel "c", usa-se o count
print(c.index(8)) #para mostrar em que posição está o número "8", usa-se o index

del(c) #para apagar uma variavel, apaga da memoria. Usa-se o "del" entre parenteses a variavel